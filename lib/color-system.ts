const seedHash = (seed: string | number) => {
  const value = String(seed);
  let hash = 2166136261;

  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }

  return hash >>> 0;
};

const randomIndex = (length: number) => Math.floor(Math.random() * length);

export const getColorVariant = (seed?: string | number | null) => {
  const hasSeed = seed !== undefined && seed !== null && `${seed}`.length > 0;
  const index = hasSeed
    ? seedHash(seed) % TAILWIND_COLOR_VARIANTS.length
    : randomIndex(TAILWIND_COLOR_VARIANTS.length);

  return TAILWIND_COLOR_VARIANTS[index];
};

export const getColorClassName = (seed?: string | number | null) =>
  getColorVariant(seed)?.className;
